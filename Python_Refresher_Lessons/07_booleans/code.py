# comparison == equal to
print(5 == 5)

# > greater than
# < less than
# != not equal
# >= greater than or equal
# <= less than or equal

# keyword conversion
friends = ["Jenny", "Abed"]
friends_abroad = ["Jenny", "Abed"]

# comparing lists with == compares contents
print(friends == friends_abroad) # True
# comparing with 'is' keyword compares both lists in memory (here they are different)
print(friends is friends_abroad) # False

